      <P align=left>Every single course offered at SEAS in 1999 and 
      beyond.</P><B><A name=numbers><I>Where did you get the 
      numbers?</I></A></B> 
      <P align=left>The numbers were received from the Dean of Engineering’s 
      office and are completely legitimate. They are the results from the 
      semesterly course evaluations that all students fill out.</P><B><A 
      name=reflection><I>Are these numbers an absolute reflection of the class 
      and professor?</I></A></B> 
      <P align=left><B></B>
      <P><U><B>NO!</B></U> Sometimes course evaluation results don’t mean 
      anything. One student holding a grudge can skew the data. The truth is 
      student opinions can vary like night and day. However, the numbers still 
      offer a certain sense and impression of the course its professor. A 
      professor receiving a 4.8 in a class of 60 students certainly must be an 
      excellent professor! Nevertheless, No one should take these numbers 
      absolutely to heart; no system is perfect after all.</P>And excerpt from 
      last year’s prologue to further the point: <I>
      <P align=left>"Although the intended purpose of evaluations is to improve 
      the quality of education, their actual value is debatable. professors who 
      grade easier tend to receive higher ratings, however, this observation 
      does not necessarily reflec t upon the true quality of teaching. Student 
      evaluations can even have a negative impact on education. There have been 
      nearly 2,000 studies conducted on the topic of evaluations, from which 
      reports in such studies, published in Change magazine and Ameri can 
      Psychologist say professors ‘who want high ratings have learned that they 
      must dumb down material, inflate grades, and keep students entertained.’" 
      </P></I><B><A name=comments><I>Why aren’t there any comments 
      included?</I></A></B> 
      <P align=left>The current publication of course evaluation results at 
      Columbia is still new and in the early stages. Last year we published only 
      3 numbers. This year we are publishing them all. Perhaps next year? 
      Furthermore, comments received in course evaluations are not always 
      constructive. An excerpt from last year’s prologue:</P><I>
      <P align=left>"…student comments often have no bearing on course quality, 
      but may rather attack the professor's personal habits or hygiene." 
</P></I>
      <P align=left>How would you feel if some published something like that 
      about you? Selecting constructive comments is a whole other issue itself. 
      </P><B><A name=surveyed><I>CCSC surveyed students to review classes, why 
      didn’t you?</I></A></B> 
      <P align=left>Surveying students is an excellent idea! Students who took 
      the surveys obviously took the class seriously and can give good feedback. 
      Perhaps we’ll do this next year? The sky is the limit.</P>
      <P align=left>There are also many problems involved with this gathering 
      scheme. For one thing reviews will be very subjective. Morevover, it is 
      nearly impossible to cover every course this way. CCSC’s course guide only 
      covered about 100 classes. We are covering about 300, or every SEAS class 
      offered in 1998. Back in 70’s and 80’s there was a large volume called the 
      <I><U>Columbia-Barnard Review</U></I> that followed the same information 
      gathering scheme as the current CCSC course guide. As you can imagine, 
      there was a gigantic amount of work involved with that operation. 
      Eventually the Columbia-Barnard review collapsed from the overwhelming 
      workload, limited manpower, and obviously lack of student feedback. 
      Getting students to actually fill the survey is a complicated task in 
      itself. The University of Pennsylvania on the other hand runs a successful 
      operation using that gathering scheme. Their program is well established 
      and in fact served as a model for us. Another model we followed was 
      Princeton University’s system. They have numbers like us AND student 
      feedback like CCSC’s course guide. Perhaps we can do this next year? The 
      best has yet to come! </P><B><A name=information><I>Where did you get the 
      professor information?</I></A></B> 
